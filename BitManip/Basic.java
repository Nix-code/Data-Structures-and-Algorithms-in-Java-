public class Basic {

  public static void main(String[] args) {
    
    int a = 5;
    int b = 6;


    System.out.println("a & b");
    System.out.println(a&b);

    System.out.println("a | b ");
    System.out.println(a|b);

    System.out.println("~b ");
    System.out.println((~b));
    System.out.println("a^b");
    System.out.println(a^b);
    System.out.println("Shift Operators");

    
    System.out.println(a << 1);
    System.out.println(b <<1);
    System.out.println(2<<2);
  }
}
