public class While{
  public static void main(String[] args){

    int i = 1;

    int n = 10;

    while(i<=n){
      System.out.println(i);
      i++;
    }

    int c = 4;
    int t=4;

    System.out.println(c++);
    System.out.println(c);
    System.out.println(++t);
    System.out.println(t);

    for(i = 1; i <10; ++i){
      System.out.println(i);

    }


  }
}
